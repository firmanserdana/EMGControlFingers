function [Letter]= LetterSelect(L)


if L == 1
   Letter = 'A';
end
if L == 2
   Letter = 'B';
end
if L == 3
   Letter = 'C';
end
if L == 4
   Letter = 'D';
end
if L == 5
   Letter = 'F';
end
if L == 6
   Letter = 'I';
end
if L == 7
   Letter = 'K';
end
if L == 8
   Letter = 'L';
end
if L == 9
   Letter = 'O';
end
if L == 10
   Letter = 'W';
end

end