function [Letter]= AngleSelect(L)


if L == 1
   Letter = 'TIF';
end
if L == 2
   Letter = 'TAP';
end
if L == 3
   Letter = 'MPF2';
end
if L == 4
   Letter = 'FIF2';
end
if L == 5
   Letter = 'MPF3';
end
if L == 6
   Letter = 'FIF3';
end
if L == 7
   Letter = 'FIF4';
end
if L == 8
   Letter = 'MPF4';
end
if L == 9
   Letter = 'FIF5';
end
if L == 10
   Letter = 'MPF5';
end
if L == 11
   Letter = 'TAB';
end

end